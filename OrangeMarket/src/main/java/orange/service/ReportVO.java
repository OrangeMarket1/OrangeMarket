package orange.service;

public class ReportVO {

	private int unq;
	private String title;
	private String content;
	private String rdate;
	private String udate;
	private String status;
	private int writer;
	private String witerNik;
	private int seller;
	private String sellerNik;
	private String reContent;
	
	
	public int getUnq() {
		return unq;
	}
	public void setUnq(int unq) {
		this.unq = unq;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getRdate() {
		return rdate;
	}
	public void setRdate(String rdate) {
		this.rdate = rdate;
	}
	public String getUdate() {
		return udate;
	}
	public void setUdate(String udate) {
		this.udate = udate;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getWriter() {
		return writer;
	}
	public void setWriter(int writer) {
		this.writer = writer;
	}
	public String getWiterNik() {
		return witerNik;
	}
	public void setWiterNik(String witerNik) {
		this.witerNik = witerNik;
	}
	public int getSeller() {
		return seller;
	}
	public void setSeller(int seller) {
		this.seller = seller;
	}
	public String getSellerNik() {
		return sellerNik;
	}
	public void setSellerNik(String sellerNik) {
		this.sellerNik = sellerNik;
	}
	public String getReContent() {
		return reContent;
	}
	public void setReContent(String reContent) {
		this.reContent = reContent;
	}
	
}
