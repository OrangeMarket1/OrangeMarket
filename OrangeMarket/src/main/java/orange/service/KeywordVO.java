package orange.service;

public class KeywordVO {
	private String keyword;
	private String rdate;
	private String srchDate;
	private String srchIp;
	
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public String getRdate() {
		return rdate;
	}
	public void setRdate(String rdate) {
		this.rdate = rdate;
	}
	public String getSrchDate() {
		return srchDate;
	}
	public void setSrchDate(String srchDate) {
		this.srchDate = srchDate;
	}
	public String getSrchIp() {
		return srchIp;
	}
	public void setSrchIp(String srchIp) {
		this.srchIp = srchIp;
	}
	
}
